// Theme preferences that can be modified by the settings
object ThemePreferences {
    var themeMode by mutableStateOf(ThemeMode.DARK)
    var useSystemTheme by mutableStateOf(false)
}

@Composable
fun GymPlannerTheme(
    darkTheme: Boolean = when {
        ThemePreferences.useSystemTheme -> isSystemInDarkTheme()
        else -> ThemePreferences.themeMode == ThemeMode.DARK
    },
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = false, // Disabled by default to use our custom colors
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            
            // Set up edge-to-edge experience
            WindowCompat.setDecorFitsSystemWindows(window, false)
            
            // Set system bars to transparent using WindowCompat APIs
            // This is the recommended way instead of using the deprecated properties
            WindowCompat.getInsetsController(window, view).apply {
                isAppearanceLightStatusBars = !darkTheme
                isAppearanceLightNavigationBars = !darkTheme
            }
            
            // Set flags for transparent system bars
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.Q) {
                window.isNavigationBarContrastEnforced = false
            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = {
            Surface(
                modifier = Modifier.fillMaxSize(),
                color = MaterialTheme.colorScheme.background
            ) {
                content()
            }
        }
    )
}

